CREATE TABLE roles (
    role_id integer GENERATED BY DEFAULT AS IDENTITY,
    role_name text NOT NULL,
    CONSTRAINT "PK_roles" PRIMARY KEY (role_id)
);

CREATE TABLE users (
    user_id uuid NOT NULL,
    email text NOT NULL,
    password_hash text NOT NULL,
    role_id integer NOT NULL,
    created_at timestamp with time zone DEFAULT (now()),
    updated_at timestamp with time zone,
    CONSTRAINT "PK_users" PRIMARY KEY (user_id),
    CONSTRAINT "FK_users_roles_role_id" FOREIGN KEY (role_id) REFERENCES roles (role_id) ON DELETE RESTRICT
);

CREATE TABLE refresh_tokens (
    refresh_token_id uuid NOT NULL,
    user_id uuid NOT NULL,
    token text NOT NULL,
    expires_at timestamp with time zone NOT NULL,
    revoked_at timestamp with time zone,
    created_at timestamp with time zone NOT NULL DEFAULT (now()),
    CONSTRAINT "PK_refresh_tokens" PRIMARY KEY (refresh_token_id),
    CONSTRAINT "FK_refresh_tokens_users_user_id" FOREIGN KEY (user_id) REFERENCES users (user_id) ON DELETE RESTRICT
);

CREATE TABLE tasks (
    task_item_id uuid NOT NULL,
    title text NOT NULL,
    description text,
    assignee_id uuid NOT NULL,
    deadline date NOT NULL,
    priority integer NOT NULL,
    status integer NOT NULL,
    created_id timestamp with time zone NOT NULL DEFAULT (now()),
    updated_id timestamp with time zone,
    CONSTRAINT "PK_tasks" PRIMARY KEY (task_item_id),
    CONSTRAINT "FK_tasks_users_assignee_id" FOREIGN KEY (assignee_id) REFERENCES users (user_id) ON DELETE RESTRICT
);

CREATE TABLE worklogs (
    worklog_id uuid NOT NULL,
    task_item_id uuid NOT NULL,
    user_id uuid NOT NULL,
    work_date date NOT NULL,
    hours_spent numeric NOT NULL,
    note text,
    created_at timestamp with time zone NOT NULL DEFAULT (now()),
    CONSTRAINT "PK_worklogs" PRIMARY KEY (worklog_id),
    CONSTRAINT ck_worklogs_hours CHECK (hours_spent > 0 AND hours_spent <= 8),
    CONSTRAINT "FK_worklogs_tasks_task_item_id" FOREIGN KEY (task_item_id) REFERENCES tasks (task_item_id) ON DELETE RESTRICT,
    CONSTRAINT "FK_worklogs_users_user_id" FOREIGN KEY (user_id) REFERENCES users (user_id) ON DELETE RESTRICT
);

